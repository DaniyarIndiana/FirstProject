package ArrayExamle;

public class ArraysStringSize4 {
    public static void main(String[] args) {
        String[] names = new String[4];
        names[0] = "Aperi";
        names[1] = "Doka";
        names[2] = "Jika";
        names[3] = "Jurabek";
        for (int i = 0; i < names.length; i++) {
            System.out.println(names[1] + "\n" + names[2]);
        }
    }

}